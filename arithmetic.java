public class arithmetic {
    public static void main(String[] args) {
        int x = 20;
        int y = 2;
        int sum = x + y;
        System.out.println("x + y = " + sum);
        int difference = x - y;
        System.out.println("x - y = " + difference);
        int product = x * y;
        System.out.println("x * y = " + product);
    }
}


